package com.kodilla.basic_assertion;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int powerOfA (int a){

        return a * a ;
    }
    public int powerOfB (int b){

        return b * b ;
    }
}